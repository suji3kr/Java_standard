package ch02.sec01.exam01;

public class Calculator {

	public static void main(String[] args) {

		
		int i = 5;
		int j = 2; 
 		
		System.out.println(i + " + "+ j+ "=" + (i+j));
		System.out.println(i + " - "+ j+ "=" + (i-j));
		System.out.println(i + " * "+ j+ "=" + (i*j));
		System.out.println(i + " / "+ j+ "=" + ((float)i+j));
		System.out.println(i + " % "+ j+ "=" + (i%j));
		
		
		
	}

}
